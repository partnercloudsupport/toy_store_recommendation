  Future googlelogin() async {
    GoogleSignInAccount googleUser = await _googleSignIn.signIn();
    GoogleSignInAuthentication googleAuth = await googleUser.authentication;
    await _auth.signInWithGoogle(
      accessToken: googleAuth.accessToken,
      idToken: googleAuth.idToken,
    );
    print("signed in ");
  }


  Future _signup(String email, String pass) async {
  try {
    await FirebaseAuth.instance
        .createUserWithEmailAndPassword(
          email: email, password: pass);
    showInSnackBar("Signup successful!");

  } catch (e) {
    debugPrint(e.toString());
    showInSnackBar(e.message);
      if(e.message=='The email address is already in use by another account.') {
      showInSnackBar('Try to signup with different account');
      }
    }
  }

  Future _login(String email, String pass) async {
    try {
      await FirebaseAuth.instance.signInWithEmailAndPassword(
        email: email, password: pass
      );
    } catch(e) {
      showInSnackBar(e.message);
    }
  }

  void signup(String name, String email, String password, String cnfpassword) {
    if(_validateName(name) &&_validateEmail(email) && _validatePassword(password, cnfpassword) ){
      _signup(email, password);
    } else {
      debugPrint('failed signing up');
    }
  }

  void login(String email, String password) {
    if(_validateEmail(email) && (_validatePassword(password, password)) ) {
      _login(email,password);
    }
  }


