    bool _validateEmail(String value) {
      if (value.isEmpty) {
        showInSnackBar('Email is required.');
        return false;
      }
      String r = r'^(([^<>()[\]\\.,;:\s@\"]+(\.[^<>()[\]\\.,;:\s@\"]+)*)|(\".+\"))@((\[[0-9]{1,3}\.[0-9]{1,3}\.[0-9]{1,3}\.[0-9]{1,3}\])|(([a-zA-Z\-0-9]+\.)+[a-zA-Z]{2,}))$';
      final RegExp emailExp = new RegExp(r);
      if (!emailExp.hasMatch(value)) {
        showInSnackBar('Please enter Valid Email');
        return false;
        }
      return true;
    }

  bool _validateName(String value) {

    if (value.isEmpty){
      showInSnackBar('Name is required.');
      
      return false;
    }
    String r = "([a-zA-Z]{3,30}\s*)+";
    final RegExp emailExp = new RegExp(r);
    if (!emailExp.hasMatch(value)) {
      showInSnackBar('Please enter Valid Name');
      return false;
    }
    return true;
  }

  bool _validatePassword(String value, String value2) {

    if(value != value2) {
      showInSnackBar('password not matching');
      return false;
    }

    if (value == null || value.isEmpty){
      showInSnackBar('Please choose a password');
      return false;
    }
    if (value.length < 8){
      showInSnackBar('Enter minimum 8 characters');
      return false;
    }
    
    if (value != value){
      showInSnackBar('Passwords don\'t match');
      return false;
    }
      
    return true;
  }

